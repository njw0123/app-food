package data.fcst;

public class FcstBody {
	String dataType;
	FcstItems items;
	int pageNo;
	int numOfRows;
	int totalCount;
	public String getDataType() {
		return dataType;
	}
	public FcstItems getItems() {
		return items;
	}
	public int getPageNo() {
		return pageNo;
	}
	public int getNumOfRows() {
		return numOfRows;
	}
	public int getTotalCount() {
		return totalCount;
	}
	
}
