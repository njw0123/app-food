package data.fcst;

public class FcstItem {
	String baseDate;
	String baseTime;
	String category;
	String fcstDate;
	String fcstTime;
	String fcstValue;
	int nx;
	int ny;
	
	public String getBaseDate() {
		return baseDate;
	}
	public String getBaseTime() {
		return baseTime;
	}
	public String getCategory() {
		return category;
	}
	public String getFcstDate() {
		return fcstDate;
	}
	public String getFcstTime() {
		return fcstTime;
	}
	public String getFcstValue() {
		return fcstValue;
	}
	public int getNx() {
		return nx;
	}
	public int getNy() {
		return ny;
	}	
	
}
