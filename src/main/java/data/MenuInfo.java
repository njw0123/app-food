package data;

public class MenuInfo {
	int id;
	String menu;
	String menucate;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getMenu() {
		return menu;
	}
	public void setMenu(String menu) {
		this.menu = menu;
	}
	public String getMenucate() {
		return menucate;
	}
	public void setMenucate(String menucate) {
		this.menucate = menucate;
	}
	
	
}
